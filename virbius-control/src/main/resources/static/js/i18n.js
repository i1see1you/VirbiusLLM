  let currentLocale = null;

  function detectLocale() {
    try {
      const saved = localStorage.getItem('virbius.i18n.locale');
      if (saved === 'zh' || saved === 'en') return saved;
    } catch (_) {}
    const params = new URLSearchParams(window.location.search);
    const langParam = params.get('lang');
    if (langParam === 'zh-CN' || langParam === 'zh' || langParam === 'en') {
      return langParam.startsWith('zh') ? 'zh' : 'en';
    }
    const navLang = (navigator.language || navigator.browserLanguage || '').toLowerCase();
    if (navLang.startsWith('zh')) return 'zh';
    return 'en';
  }

  function setLocale(locale) {
    if (locale !== 'zh' && locale !== 'en') locale = 'en';
    currentLocale = locale;
    window._locale = locale;
    document.documentElement.lang = locale === 'zh' ? 'zh-CN' : 'en';
    try { localStorage.setItem('virbius.i18n.locale', locale); } catch (_) {}
    applyI18nToDOM();
    const btn = document.getElementById('btnLangSwitch');
    if (btn) btn.textContent = I18N[locale]['topbar.lang'];
  }

  function __(key, ...args) {
    if (!currentLocale) currentLocale = detectLocale();
    let val = I18N[currentLocale] && I18N[currentLocale][key];
    if (val == null) {
      val = I18N.en[key];
    }
    if (val == null) return '?' + key;
    if (args.length > 0) {
      args.forEach((arg, i) => {
        val = val.replace(new RegExp('\\{' + i + '\\}', 'g'), String(arg != null ? arg : ''));
      });
    }
    return val;
  }

  function applyI18nToDOM() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      const attr = el.dataset.i18nAttr;
      if (attr) {
        el.setAttribute(attr, __(key));
      } else {
        const isHtml = el.dataset.i18nHtml !== undefined;
        if (isHtml) {
          el.innerHTML = __(key);
        } else {
          el.textContent = __(key);
        }
      }
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const txt = __(el.dataset.i18nHtml);
      const children = Array.from(el.children);
      if (children.length === 0 || !txt.includes('{')) {
        el.innerHTML = txt;
      } else {
        const parts = txt.split(/(\{\d+\})/);
        el.innerHTML = '';
        for (const part of parts) {
          const m = part.match(/^\{(\d+)\}$/);
          if (m) {
            const idx = parseInt(m[1], 10);
            if (idx < children.length) el.appendChild(children[idx]);
          } else {
            el.appendChild(document.createTextNode(part));
          }
        }
      }
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = __(el.dataset.i18nPlaceholder);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      el.title = __(el.dataset.i18nTitle);
    });
  }

  window.__ = __;
  window.setLocale = setLocale;
  window.detectLocale = detectLocale;
  window.applyI18nToDOM = applyI18nToDOM;

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => setLocale(detectLocale()));
  } else {
    setLocale(detectLocale());
  }
